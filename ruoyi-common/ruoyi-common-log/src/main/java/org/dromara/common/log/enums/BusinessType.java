package org.dromara.common.log.enums;

/**
 * 业务操作类型枚举
 * 定义系统中各种业务操作的类型分类，用于操作日志记录
 * 通过枚举值标识不同的业务操作行为
 *
 * @author ruoyi
 */
public enum BusinessType {
    /**
     * 其他类型
     * 表示未知或未分类的业务操作
     */
    OTHER,

    /**
     * 新增操作
     * 表示创建新记录的业务操作
     */
    INSERT,

    /**
     * 修改操作
     * 表示更新现有记录的业务操作
     */
    UPDATE,

    /**
     * 删除操作
     * 表示删除记录的业务操作
     */
    DELETE,

    /**
     * 授权操作
     * 表示权限分配的业务操作
     */
    GRANT,

    /**
     * 导出操作
     * 表示数据导出的业务操作
     */
    EXPORT,

    /**
     * 导入操作
     * 表示数据导入的业务操作
     */
    IMPORT,

    /**
     * 强制退出操作
     * 表示强制用户下线的业务操作
     */
    FORCE,

    /**
     * 生成代码操作
     * 表示代码生成的业务操作
     */
    GENCODE,

    /**
     * 清空数据操作
     * 表示清空数据的业务操作
     */
    CLEAN,
}
