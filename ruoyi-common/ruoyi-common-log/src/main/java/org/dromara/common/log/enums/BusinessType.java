package org.dromara.common.log.enums;

/**
 * 业务操作类型枚举
 * 定义系统中各种业务操作的类型分类，用于操作日志记录
 * 通过枚举值标识不同的业务操作行为，便于日志统计和分类查询
 *
 * @author ruoyi
 */
public enum BusinessType {
    /**
     * 其他类型
     * 表示未知或未分类的业务操作
     * 默认类型，当无法归类到其他具体类型时使用
     */
    OTHER,

    /**
     * 新增操作
     * 表示创建新记录的业务操作
     * 如：新增用户、新增菜单、新增部门等
     */
    INSERT,

    /**
     * 修改操作
     * 表示更新现有记录的业务操作
     * 如：修改用户信息、修改配置、更新状态等
     */
    UPDATE,

    /**
     * 删除操作
     * 表示删除记录的业务操作
     * 如：删除用户、删除菜单、逻辑删除或物理删除
     */
    DELETE,

    /**
     * 授权操作
     * 表示权限分配的业务操作
     * 如：分配角色权限、分配用户角色、数据权限配置等
     */
    GRANT,

    /**
     * 导出操作
     * 表示数据导出的业务操作
     * 如：导出Excel、导出PDF、导出CSV等
     */
    EXPORT,

    /**
     * 导入操作
     * 表示数据导入的业务操作
     * 如：从Excel导入数据、批量导入用户等
     */
    IMPORT,

    /**
     * 强制退出操作
     * 表示强制用户下线的业务操作
     * 如：强制踢出在线用户、会话管理等
     */
    FORCE,

    /**
     * 生成代码操作
     * 表示代码生成的业务操作
     * 如：使用代码生成器生成CRUD代码
     */
    GENCODE,

    /**
     * 清空数据操作
     * 表示清空数据的业务操作
     * 如：清空日志、清空缓存、重置数据等
     */
    CLEAN,
}
